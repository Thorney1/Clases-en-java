public class Clientes {
    string rut;
    int idCliente;
    string nombre;
    string direccion;
    int celular;
    string email;

    public Clientes(string rut, int idCliente, string nombre, string direccion, int celular, string email){
        this.rut = rut;
        this.idCliente = idCliente;
        this.nombre = nombre;
        this.direccion = direccion;
        this.celular = celular;
        this.email = email;
    }
}
